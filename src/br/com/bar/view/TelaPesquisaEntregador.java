    private void btnAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdicionarMouseClicked
        
       // Chama tela de cadastrode entregador
        TelaCadastroEntregador tce = new TelaCadastroEntregador();
        tce.recebeOperador(this, lblOperador.getText(), lblCargo.getText(), "Adicionar");
        tce.setVisible(true);
    }//GEN-LAST:event_btnAdicionarMouseClicked

    private void tbEntregadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEntregadorMouseClicked
        
        int linha = tbEntregador.getSelectedRow();
        String nomeEntregador = tbEntregador.getModel().getValueAt(linha, 0).toString();
        entregador = ce.localizaEntregador(nomeEntregador);        
        desbloqueiaBotoes();
        
    }//GEN-LAST:event_tbEntregadorMouseClicked

    private void btnConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConsultarMouseClicked
        if (btnConsultar.isEnabled()){
            
            if (entregador == null) {
                JOptionPane.showMessageDialog(this, "Selecione um Entregador para continuar!", "Atenção", JOptionPane.ERROR_MESSAGE);
            } else {
                TelaCadastroEntregador tce = new TelaCadastroEntregador();
                tce.recebeOperador(this, lblOperador.getText(), lblCargo.getText(), "Consultar");
                tce.recebeEntregador(entregador);
                tce.setVisible(true);
                estadoInicial();
            }
        }
    }//GEN-LAST:event_btnConsultarMouseClicked

    private void btnAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlterarMouseClicked
        
        if (btnAlterar.isEnabled()){
            TelaCadastroEntregador cadEntregador = new TelaCadastroEntregador();
            cadEntregador.recebeOperador(this, lblOperador.getText(), lblCargo.getText(), "Alterar");
            cadEntregador.recebeEntregador(entregador);
            cadEntregador.setVisible(true);
            estadoInicial();
        }
    }//GEN-LAST:event_btnAlterarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // Exclui um entregador
        if (btnExcluir.isEnabled()){
            int resp = JOptionPane.showConfirmDialog(this,"Confirma a exclusão do entregador "+ entregador.getNome() + " ?", "Atenção!",JOptionPane.YES_NO_OPTION,JOptionPane.ERROR_MESSAGE);
            if (resp==JOptionPane.YES_OPTION){
                if (ce.excluiEntregador(entregador)) {
                    JOptionPane.showMessageDialog(this, "Entregador excluído com sucesso!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                    atualizaTabela();
                }                
            }
        }
    }//GEN-LAST:event_btnExcluirMouseClicked

    public void recebeOperador(String operador, String cargo){
        lblCargo.setText(cargo);
        lblOperador.setText(operador);
    }
    
