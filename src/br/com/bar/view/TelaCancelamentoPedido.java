    private void extornar() {
         NFCeCancelamento c = new NFCeCancelamento();
            Log l = new Log();
            int op = JOptionPane.showOptionDialog(this, "Confirma o extrono do Pedido N.° " + txtNumeroPedido.getText() + "?", "Atenção!",
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE, null, opcao, opcao[1]);
            if (op == 1) {
                String msg = controlPedido.extornaPedido(txtNumeroPedido.getText(), operador);
                JOptionPane.showMessageDialog(this, msg);
                txtNumeroPedido.setText(null);
                lblBtnCancelar.setEnabled(false);
            }
    }
