    private void btnAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdicionarMouseClicked

        Localidade l = new Localidade();
        l.setNome(txtLocalidade.getText());

        if ("".equals(txtLocalidade.getText())) {
            JOptionPane.showMessageDialog(this, "Informe uma localidade para continuar!", "Ate√ß√£o!", JOptionPane.ERROR_MESSAGE);
            txtLocalidade.requestFocus();
        } else {
            if ("".equals(txtTaxa.getText())) {
                JOptionPane.showMessageDialog(this, "Informe um valor para continuar!", "Aten√ß√£o!", JOptionPane.ERROR_MESSAGE);
                txtTaxa.requestFocus();
            } else {
                // Converte para double
                l.setTaxa(Double.parseDouble(txtTaxa.getText().replace(",", ".")));
                // Checa se a localidade Existe
                if (cl.temLocalidade(l)) {
                    JOptionPane.showMessageDialog(this, "Esta localidade j√° existe!", "Aten√ß√£o!", JOptionPane.ERROR_MESSAGE);
                } else {
                    //Adiciona a lodalidade no banco
                    try {
                        
                        double tx = Double.parseDouble(txtTaxa.getText().replace(",", "."));
                        if (tx<=0) {
                            JOptionPane.showMessageDialog(this, "Valor Inv√°lido!", "Aten√ß√£o!", JOptionPane.INFORMATION_MESSAGE);
                            txtTaxa.setText(null);
                        } else {
                            if (cl.adicionaLocalidade(l)) {
                                JOptionPane.showMessageDialog(this, "Localidade adicionada com sucesso!", "Aten√ß√£o!", JOptionPane.INFORMATION_MESSAGE);
                                atualizaTabela();

                                limpaForm();
                                btnExcluir.setEnabled(false);
                                btnAlterar.setEnabled(false);
                            }

                        }
                    } catch (HeadlessException | NumberFormatException e) {
                        System.out.println("br.com.bar.view.TelaLocalidade.btnAdicionarMouseClicked()"+e);
                         JOptionPane.showMessageDialog(this, "Valor Inv√°lido!", "Aten√ß√£o!", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }

    }//GEN-LAST:event_btnAdicionarMouseClicked

    private void btnAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAlterarMouseClicked
            
      if (btnAlterar.isEnabled()){
          
          double tx = Double.parseDouble(txtTaxa.getText().replace(",", "."));
          int id = Integer.parseInt(txtIdLocalidade.getText());
          if (tx > 0) {
              Localidade l = new Localidade(id, txtLocalidade.getText(), tx);
              int op = JOptionPane.showConfirmDialog(this, "Deseja mesmo alterar esta localidade?", "Aten√ß√£o", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
              if (op == JOptionPane.YES_OPTION) {
                  if (cl.alteraLocalidade(l)) {
                      JOptionPane.showMessageDialog(this, "Localidade alterada com sucesso!", "Aten√ß√£o!", JOptionPane.INFORMATION_MESSAGE);
                      limpaForm();
                      estadoInicial(false);
                      atualizaTabela();
                  }

              } else {
                  limpaForm();
                  estadoInicial(false);
              }
          }
  
      }

    }//GEN-LAST:event_btnAlterarMouseClicked

    private void txtTaxaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTaxaKeyPressed
        if (evt.getKeyCode()==java.awt.event.KeyEvent.VK_ENTER){
            try {
                
                double taxa = Double.parseDouble(txtTaxa.getText().replace(",", "."));
                txtTaxa.setText(String.format("%9.2f", taxa));
            } catch (NumberFormatException e) {
                 JOptionPane.showMessageDialog(this, "Valor inv√°lido!", "Aten√ß√£o!", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_txtTaxaKeyPressed

