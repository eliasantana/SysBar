    private void geraGrafico() {
        OutputStream arquivo;
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        ControlerContasApagar cc = new ControlerContasApagar();
        Util u = new Util();
        String inicio = u.formataDataBanco(jdateInicio.getDate());
        String fim = u.formataDataBanco(JdateFim.getDate());
        ResultSet rs = cc.listaContasPorCategoriaPeriodo(inicio, fim);

        try {
            while (rs.next()) {
                dataset.setValue(rs.getDouble("valor"), rs.getString("grupo"), "");
                System.out.println(rs.getString("grupo") + " " + rs.getString("valor"));
            }
        } catch (SQLException e) {
        }

        JFreeChart graficoDeBarras = ChartFactory.createBarChart("Grafico por Categoria", "Grupo", "Valor", dataset);

        try {
            File f = new File("c:/sysBar/graficoBarrasGruposDespesa.png");
            f.delete();
            arquivo = new FileOutputStream("c:/sysBar/graficoBarrasGruposDespesa.png");
            try {
                
                ChartUtilities.writeChartAsPNG(arquivo, graficoDeBarras, 500, 400);
                //arquivo.close();

            } catch (IOException ex) {
                Logger.getLogger(TeteJFreeChart.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(TeteJFreeChart.class.getName()).log(Level.SEVERE, null, ex);
        }
         atualizaGrafico();
    }

    private void atualizaGrafico() {

        Icon i = new ImageIcon("c:/sysBar/graficoBarrasGruposDespesa.png");
        lblGraficoGrupoDespesas.setIcon(i);

    }

