    private void txtNumeroNotaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumeroNotaKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtNumeroNota.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Informe o número da nota", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }else{
                // Verifica antes de proseguir se o cupom informado está cancelado
                if (!cnfec.estaCancelada(txtNumeroNota.getText())) {
                    txtEmail1.setEnabled(true);
                    txtEmail1.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Não é possível realizar o envio de cupom cancelado!", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }                
            }

        }
    }//GEN-LAST:event_txtNumeroNotaKeyPressed

    private void lblBtnEnviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBtnEnviarMouseClicked

        if (lblBtnEnviar.isEnabled()) {
            // Verifica localmente se a nota está cancelada, se estiver exibe mensagem ao usuário
            ConexaoInternet i = new ConexaoInternet();
            // Avisa ao usuário se existe conexao com a internet
            if (i.temConexao()) {
                ArrayList<String> listaEmail = new ArrayList<>();

                if (!txtEmail1.getText().isEmpty()) {
                    listaEmail.add(txtEmail1.getText().toLowerCase());
                }

                if (!txtEmail2.getText().isEmpty()) {
                    listaEmail.add(txtEmail2.getText().toLowerCase());
                }
                // Envia a lista com os emails informados
                try {
                    if (cnfec.enviaEmail(txtNumeroNota.getText(), listaEmail)){
                        JOptionPane.showMessageDialog(this,"E-mail(s) enviado(s) com sucesso!\n Obs: O(s) E-mail(s) levarão alguns minutos até serem entregues em sua(s) caixas postais!");
                        txtNumeroNota.setText(null);
                        txtEmail1.setText(null);
                        txtEmail1.setEnabled(false);
                        txtEmail2.setText(null);
                        txtEmail2.setEnabled(false);
                        lblBtnEnviar.setEnabled(false);
                        e1.setVisible(false);
                        e2.setVisible(false);
                        x1.setVisible(false);
                        x2.setVisible(false);
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(this,"Cupom fiscal não localizado!","Atenção",JOptionPane.ERROR_MESSAGE);
                    }
                } catch (JSONException ex) {
                    System.out.println("br.com.bar.view.TelaEnviaNFCe.lblBtnEnviarMouseClicked()" + ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sem conexao com a internet!\nNão é possível enviar o cupom neste momento! Tente novamente mais tarde.", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_lblBtnEnviarMouseClicked

    private void txtEmail1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmail1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (u.validaEmail(txtEmail1.getText().toLowerCase())) {
                e1.setVisible(true);
                x1.setVisible(false);
                int op = JOptionPane.showOptionDialog(this, "Deseja adicionar outro email?", "Atenção!", 
                         JOptionPane.YES_OPTION, JOptionPane.INFORMATION_MESSAGE,null,opcao, opcao[1]);

                if (op == 0) {
                    txtEmail2.setEnabled(true);
                    txtEmail2.requestFocus();
                } else {
                    lblBtnEnviar.setEnabled(true);
                }
            } else {
                x1.setVisible(true);
                e1.setVisible(false);
                txtEmail2.setEnabled(false);
                txtEmail1.requestFocus();
            }
        }
    }//GEN-LAST:event_txtEmail1KeyPressed

