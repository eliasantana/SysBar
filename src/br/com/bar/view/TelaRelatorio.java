    private void btnOK1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOK1ActionPerformed
        int opcao = comboTipo.getSelectedIndex();

        HashMap map = new HashMap();

        if (0 == opcao) {
            try {

                JasperPrint print = JasperFillManager.fillReport(p.getRELATORIOS() + "relGeralDeFuncionarios.jasper", null, conexao);
                JasperViewer.viewReport(print, false);

                // Início do registro de Log
                l.setFuncionalidade("Relatório");
                l.setDescricao(l.getUsuario() + " acessou o " + l.getFuncionalidade() + " geral de Funcionários");
                l.gravaLog(l);
                // Fim do registro de Log

            } catch (JRException ex) {
                Logger.getLogger(TelaRelatorio.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {

            switch (opcao) {
                case 1:
                    map.put("b", 1); // Funcionários Inativo
                    break;
                case 2:
                    map.put("b", 0); // Funcionarios Ativos
                    break;

            }

            try {
                JasperPrint print = JasperFillManager.fillReport(p.getRELATORIOS() + "relFuncBlocDesb.jasper", map, conexao);
                JasperViewer.viewReport(print, false);
                // Início do registro de Log
                l.setFuncionalidade("Relatório");
                l.setDescricao(l.getUsuario() + " acessou o " + l.getFuncionalidade() + " relatório geral de funcionários");
                l.gravaLog(l);
                // Fim do registro de Log
            } catch (JRException ex) {
                Logger.getLogger(TelaRelatorio.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnOK1ActionPerformed

