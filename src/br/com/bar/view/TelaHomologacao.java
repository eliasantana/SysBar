    private void panelHomologaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelHomologaMouseClicked
                // Captura linha selecionada
        
        int linha = tbHomologacao.getSelectedRow();
        
        if (linha < 0){
            JOptionPane.showMessageDialog(null, "É necessário selecionar um ponnto \n para cotinuar!");
        }else {
            
            // Realiza Homologação Individual
            // Instancia objeto Ponto      
            Ponto p = new Ponto();
            // Seta  dados
            p.setTbcadfuncionario_id(Integer.parseInt(tbHomologacao.getModel().getValueAt(linha, 1).toString()));
            p.setData(u.formataDataBanco(tbHomologacao.getModel().getValueAt(linha, 3).toString()));
            p.setH_trab(cp.calculoHoraTrab(p));
            p.setHomologado(1);
            // Registra hora trabalhada
            cp.registraHoraTrab(p);
            p.setH_extra(cp.calculaExtra(p));
            // Registra a hora Extra_trabalhda
            cp.registraHoraExtra(p);
            // Atualiza tabela
            cp.listaPonto(tbHomologacao);
        }
    }//GEN-LAST:event_panelHomologaMouseClicked

