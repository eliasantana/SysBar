    private void btnInformarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInformarMouseClicked
        
        
        if (Util.isCPF(txtCpf.getText())){
            System.out.println("CPF: " + txtCpf.getText());
            String cpf = txtCpf.getText();
            cpf = cpf.replace(".", "");
            cpf = cpf.replace("-", "");
                
            System.out.println("CPF: "+cpf);   
            this.setVisible(false);
            tela.receber(cpf,txtCpf.getText());            
            this.dispose();
            
        }else {
            JOptionPane.showMessageDialog(this,"O CPF informado não é válido!");
        }
    }//GEN-LAST:event_btnInformarMouseClicked

